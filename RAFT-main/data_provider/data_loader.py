def _compact_numeric_meta(meta_data):
    """Keep only retrieval-required numeric fields for safe DataLoader collation."""
    local_state = np.asarray(meta_data.get("local_state_by_period", np.zeros((3, 4), dtype=np.float32)), dtype=np.float32)
    if local_state.ndim == 1:
        local_state = local_state.reshape(1, -1)
    if local_state.shape[0] < 3:
        pad = np.zeros((3 - local_state.shape[0], local_state.shape[1]), dtype=np.float32)
        local_state = np.concatenate([local_state, pad], axis=0)
    if local_state.shape[1] < 4:
        pad = np.zeros((local_state.shape[0], 4 - local_state.shape[1]), dtype=np.float32)
        local_state = np.concatenate([local_state, pad], axis=1)
    local_state = local_state[:3, :4].astype(np.float32)

    compact = {
        "dataset_id": int(meta_data.get("dataset_id", 0)),
        "sensor_type_id": int(meta_data.get("sensor_type_id", 0)),
        "physical_location_id": int(meta_data.get("physical_location_id", 0)),
        "hour": int(meta_data.get("hour", 0)),
        "day_of_week": int(meta_data.get("day_of_week", 0)),
        "month": int(meta_data.get("month", 1)),
        "is_holiday": int(meta_data.get("is_holiday", 0)),
        "peak_status_id": int(meta_data.get("peak_status_id", 0)),
        "local_state_by_period": local_state,
    }
    return compact


def build_sample_meta(dataset, index, seq_x):
    s_end = index + dataset.seq_len
    anchor_idx = min(s_end - 1, len(dataset.date_index) - 1)
    timestamp = dataset.date_index.iloc[anchor_idx]
    meta_data, _, _ = build_meta_record(
        timestamp=timestamp,
        dataset_name=dataset.dataset_name,
        target=dataset.target,
        feature_names=dataset.feature_names,
        seq_x=seq_x,
        prompt_factory=None,
        build_text=False,
    )
    return _compact_numeric_meta(meta_data)


class Dataset_ETT_hour(Dataset):
    def __init__(self, args, root_path, flag='train', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='OT', scale=True, timeenc=0, freq='h', seasonal_patterns=None):
        # size [seq_len, label_len, pred_len]
        self.args = args
        # info
        if size == None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]
        # init
        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path,
                                          self.data_path))

        border1s = [0, 12 * 30 * 24 - self.seq_len, 12 * 30 * 24 + 4 * 30 * 24 - self.seq_len]
        border2s = [12 * 30 * 24, 12 * 30 * 24 + 4 * 30 * 24, 12 * 30 * 24 + 8 * 30 * 24]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        if self.scale:
            train_data = df_data[border1s[0]:border2s[0]]
            self.scaler.fit(train_data.values)
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values

        df_stamp = df_raw[['date']][border1:border2]
        df_stamp['date'] = pd.to_datetime(df_stamp.date)
        if self.timeenc == 0:
            df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)
            df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
            df_stamp['weekday'] = df_stamp.date.apply(lambda row: row.weekday(), 1)
            df_stamp['hour'] = df_stamp.date.apply(lambda row: row.hour, 1)
            data_stamp = df_stamp.drop(['date'], 1).values
        elif self.timeenc == 1:
            data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq)
            data_stamp = data_stamp.transpose(1, 0) 
        data_stamp = np.asarray(data_stamp, dtype=np.float32)

        self.data_x = data[border1:border2]
        self.data_y = data[border1:border2]

        if self.set_type == 0 and self.args.augmentation_ratio > 0:
            self.data_x, self.data_y, augmentation_tags = run_augmentation_single(self.data_x, self.data_y, self.args)

        self.data_stamp = data_stamp
        self.date_index = pd.to_datetime(df_stamp['date']).reset_index(drop=True)
        self.dataset_name = infer_dataset_name(self.args, self.data_path)
        self.feature_dim = self.data_x.shape[1]
        self.feature_names = list(df_data.columns)

    def __getitem__(self, index):
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = np.asarray(self.data_x[s_begin:s_end], dtype=np.float32)
        seq_y = np.asarray(self.data_y[r_begin:r_end], dtype=np.float32)
        seq_x_mark = np.asarray(self.data_stamp[s_begin:s_end], dtype=np.float32)
        seq_y_mark = np.asarray(self.data_stamp[r_begin:r_end], dtype=np.float32)
        meta_data = build_sample_meta(self, index, seq_x)

        return index, seq_x, seq_y, seq_x_mark, seq_y_mark, meta_data

    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)


